        <h3>Literalism: Reading Machines Reading</h3>
        <p><i>Literalism</i> traces the contemporary prevalence of large,
        statistical language models (of the kind, for example, that enable
        everything from topic modeling to ChatGPT) back to a pivotal moment in
        the history of computing, when researchers broadly began to think of
        computers not just as number crunchers, but as generalized symbolic
        processors that have "access" to meaning. Through a blend of media
        archaeology, literary close reading, and computational text analysis, I
        chart how an entire scriptural economy has emerged since the early
        1950s to support this change in mindset. From blocky, machine-readable
        typefaces to escape characters, n-grams, and document-term matrices,
        various quasi-linguistic formalisms have become a key way by which
        researchers remediate natural language into computationally tractable
        forms. If today, semantic models are able to provide us with new
        findings about the nature of language, <i>Literalism</i> shows how such
        findings are first of all attributable to a skein of strange,
        intermediary representations of text that never quite present the
        original features of the data from which they derive.</p>
        <p>Measuring out the peculiarities of these representations can attune
        us, I argue, to an interpretive bent that runs through what is typically
        understood to be the mundane, unthinking work of data wrangling – text
        cleaning, tokenization, and other such procedures that convert language
        into forms readable by digital technologies. By challenging
        distinctions between technical and intellectual decisions within this
        stratum of data processing, <i>Literalism</i> thus works to critically
        assess the claims on meaning that statistical language models can and
        cannot support.</p>
